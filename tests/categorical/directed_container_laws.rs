#[test]
fn is_pure_memory_partitions_directed_from_indexed() {
    // is_pure_memory is the exact predicate that tells us where the
    // Bus is a directed container vs where it's an indexed monad.
    let bus = test_bus();

    // Pure memory addresses: directed container laws hold
    assert!(bus.is_pure_memory(0x7E, 0x0000)); // WRAM
    assert!(bus.is_pure_memory(0x00, 0x0000)); // WRAM mirror
    assert!(bus.is_pure_memory(0x00, 0x8000)); // ROM
    assert!(bus.is_pure_memory(0x70, 0x0000)); // SRAM

    // Impure addresses: directed container laws may fail
    assert!(!bus.is_pure_memory(0x00, 0x2100)); // PPU regs
    assert!(!bus.is_pure_memory(0x00, 0x2140)); // APU ports
    assert!(!bus.is_pure_memory(0x00, 0x2180)); // WMDATA
    assert!(!bus.is_pure_memory(0x00, 0x4210)); // RDNMI
    assert!(!bus.is_pure_memory(0x00, 0x4016)); // Joypad
}

// ═══════════════════════════════════════════════════════════════════
// LAW 3 + FUNCTOR LAW: bank mirroring is a natural transformation
//
// The mirroring operation (bank & 0x7F) is an endomorphism on the
// address container. For it to be a natural transformation, it must
// commute with read: read(bank, addr) = read(bank & 0x7F, addr).
// This is stronger than just testing `down` — it verifies the
// actual data path.
// ═══════════════════════════════════════════════════════════════════

#[test]
fn bank_mirroring_naturality_wram() {
    let mut bus = test_bus();
    bus.write(0x7E, 0x0010, 0xEE);
    // Read through $00 and $80 — both must see the same WRAM byte
    assert_eq!(bus.read(0x00, 0x0010), bus.read(0x80, 0x0010),
        "Bank mirror naturality: WRAM read must commute with mirroring");
}

#[test]
fn bank_mirroring_naturality_rom() {
    let mut bus = test_bus();
    assert_eq!(bus.read(0x00, 0x8000), bus.read(0x80, 0x8000),
        "Bank mirror naturality: ROM read must commute with mirroring");
    assert_eq!(bus.read(0x01, 0x8000), bus.read(0x81, 0x8000),
        "Bank mirror naturality: ROM read (bank 1) must commute");
}

#[test]
fn bank_mirroring_naturality_sram() {
    let mut bus = test_bus();
    bus.write(0x70, 0x0000, 0xDD);
    assert_eq!(bus.read(0x70, 0x0000), bus.read(0xF0, 0x0000),
        "Bank mirror naturality: SRAM read must commute with mirroring");
}

#[test]
fn bank_mirroring_is_idempotent_morphism() {
    // mirror ∘ mirror = mirror (idempotent endomorphism)
    // (bank & 0x7F) & 0x7F = bank & 0x7F
    for bank in 0..=255u8 {
        assert_eq!(bank & 0x7F, (bank & 0x7F) & 0x7F,
            "Bank mirroring must be idempotent: bank ${:02X}", bank);
    }
}
