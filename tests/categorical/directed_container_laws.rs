// ═══════════════════════════════════════════════════════════════════
// LAW 5: (p + q) + r = p + (q + r)
// "Position embedding is associative"
//
// For the Bus, position embedding is address arithmetic within a
// region. This tests whether multi-step address computation
// (bank base + offset + sub-offset) is associative.
// ═══════════════════════════════════════════════════════════════════

#[test]
fn law5_wram_offset_associative() {
    // WRAM addressing: base + offset should be associative
    // (0x7E:0) + 0x100 + 0x10 = (0x7E:0) + (0x100 + 0x10)
    let mut bus = test_bus();
    bus.write(0x7E, 0x0110, 0xAA);

    // Path 1: start at 0, step by 0x100, then by 0x10
    let addr1 = 0u16.wrapping_add(0x100).wrapping_add(0x10);
    // Path 2: start at 0, step by 0x110
    let addr2 = 0u16.wrapping_add(0x110);

    assert_eq!(addr1, addr2, "Address arithmetic should be associative");
    assert_eq!(bus.read(0x7E, addr1), bus.read(0x7E, addr2),
        "WRAM read through associative address paths must agree");
}

#[test]
fn law5_lorom_bank_offset_associative() {
    // LoROM: (bank * 0x8000) + (addr - 0x8000) should be associative
    // bank=0, addr=0x8100: offset = 0*0x8000 + (0x8100 - 0x8000) = 0x100
    // Verify this equals computing the parts separately
    let bank: u32 = 0;
    let addr: u32 = 0x8100;
    let offset_direct = bank * 0x8000 + (addr - 0x8000);

    let step1 = bank * 0x8000;     // = 0
    let step2 = addr - 0x8000;      // = 0x100
    let offset_stepped = step1 + step2;

    assert_eq!(offset_direct, offset_stepped,
        "LoROM formula must be associative: direct vs stepped computation");
}

#[test]
fn law5_sram_offset_associative() {
    // SRAM: ((bank - 0x70) * 0x8000) + addr
    // Test with bank=0x70, addr=0x100 (within first 32KB SRAM bank)
    let mut bus = test_bus();
    bus.write(0x70, 0x0100, 0xCC);

    // Compute offset two ways
    let offset1 = ((0x70u32 - 0x70) * 0x8000) + 0x100;
    let offset2 = 0u32 + 0x100;
    assert_eq!(offset1, offset2, "SRAM offset calculation must be associative");

    // And verify the bus agrees
    assert_eq!(bus.read(0x70, 0x0100), 0xCC);
}

// ═══════════════════════════════════════════════════════════════════
// LAW 4: o(s) + p = p
// "Root is identity for embedding"
//
// The "root" of the Bus address space is the reset vector location:
// $00:FFFC (where PC starts). Embedding any position p through the
// root should give back p unchanged.
//
// In practice, this means: reading the reset vector, then following
// that address, is the canonical entry point. The root doesn't
// "shift" other addresses.
// ═══════════════════════════════════════════════════════════════════

#[test]
fn law4_bank_zero_is_identity_base() {
    // Bank 0 acts as the identity for bank mirroring:
    // (0 & 0x7F) | bank_offset = bank_offset (trivially)
    // The root bank doesn't shift positions
    for &addr in &[0x0000u16, 0x1000, 0x2100, 0x2140, 0x4200, 0x8000] {
        let (r1, o1) = down(0x00, addr);
        let (r2, o2) = down(0x00 & 0x7F, addr); // identity: 0 & 0x7F = 0
        assert_eq!((r1, o1), (r2, o2),
            "Root bank (0) must be identity: addr ${:04X}", addr);
    }
}

// ═══════════════════════════════════════════════════════════════════
// LAW 1: down(s, o(s)) = s
// "Subshape at root is the whole shape"
//
// If the root is the reset vector ($00:FFFC), then the sub-address-space
// visible from the reset vector should be... the full address space.
// Since the CPU at the reset vector can reach any address, this holds
// trivially. But it's worth verifying that the reset vector address
// itself decodes to a valid region (ROM).
// ═══════════════════════════════════════════════════════════════════

#[test]
fn law1_root_decodes_to_rom() {
    // The reset vector lives at $00:FFFC-FFFD, which is in ROM
    let (region, _) = down(0x00, 0xFFFC);
    assert_eq!(region, Region::Rom,
        "Reset vector address must decode to ROM region");
}

#[test]
fn law1_root_is_reachable() {
    // Verify the reset vector is readable and contains a valid address
    let mut bus = test_bus();
    let lo = bus.read(0x00, 0xFFFC) as u16;
    let hi = bus.read(0x00, 0xFFFD) as u16;
    let reset_pc = lo | (hi << 8);
    // Our test ROM has reset vector = $8000
    assert_eq!(reset_pc, 0x8000,
        "Reset vector should point to $8000 (first ROM instruction)");
}

// ═══════════════════════════════════════════════════════════════════
// SIDE-EFFECTING READS: Where the directed container structure breaks
//
// These tests document where `down` is NOT a pure function — reading
// certain registers mutates the bus state, meaning the shape changes
// after extraction. This is where the Bus fails to be a classical
// directed container and instead lives in an indexed/dependent category.
// ═══════════════════════════════════════════════════════════════════

#[test]
fn side_effect_rdnmi_mutates_shape() {
    // Reading $4210 (RDNMI) clears nmi_flag.
    // This means extract at position ($00, $4210) changes the shape.
    // Law 1 requires down(s, o(s)) = s — but if extract mutates s,
    // then s' ≠ s after extraction, violating the law.
    let mut bus = test_bus();
    bus.nmi_flag = true;

    // First read: returns flag set, clears it
    let v1 = bus.read(0x00, 0x4210);
    assert!(v1 & 0x80 != 0, "First RDNMI read should see flag set");
    assert!(!bus.nmi_flag, "RDNMI read should clear the flag");

    // Second read: flag is now clear — different value from same position
    let v2 = bus.read(0x00, 0x4210);
    assert!(v2 & 0x80 == 0, "Second RDNMI read should see flag clear");

    // This demonstrates: extract is NOT idempotent at this position.
    // The Bus is NOT a directed container at MMIO positions.
    assert_ne!(v1, v2,
        "RDNMI: consecutive reads return different values — \
         extract is not idempotent, directed container Law 1 fails here");
}

#[test]
fn side_effect_timeup_mutates_shape() {
    // $4211 (TIMEUP) — same pattern as RDNMI
    let mut bus = test_bus();
    bus.irq_flag = true;

    let v1 = bus.read(0x00, 0x4211);
    let v2 = bus.read(0x00, 0x4211);
    assert_ne!(v1, v2,
        "TIMEUP: read-clear behavior breaks extract idempotency");
}

#[test]
fn side_effect_wmdata_mutates_position() {
    // $2180 (WMDATA) — read auto-increments wram_addr.
    // This is even more severe: the read changes which WRAM byte
    // the next read will see. The position ITSELF shifts.
    let mut bus = test_bus();
    bus.wram[0] = 0xAA;
    bus.wram[1] = 0xBB;
    bus.wram_addr = 0;

    let v1 = bus.read(0x00, 0x2180); // reads wram[0], advances to 1
    let v2 = bus.read(0x00, 0x2180); // reads wram[1], advances to 2
    assert_eq!(v1, 0xAA);
    assert_eq!(v2, 0xBB);
    assert_ne!(v1, v2,
        "WMDATA: same address returns different data — \
         the position embedding itself is stateful");
}

#[test]
fn side_effect_joypad_serial_mutates_position() {
    // $4016 (Joypad serial) — each read returns the next bit.
    // Like WMDATA, the position shifts on read.
    let mut bus = test_bus();
    bus.joypad.set_button(0x8000, true); // B button
    bus.joypad.write_strobe(1);
    bus.joypad.write_strobe(0); // falling edge latches

    let _v1 = bus.read(0x00, 0x4016);
    let _v2 = bus.read(0x00, 0x4016);
    // First bit is B (bit 15, set), second is Y (bit 14, not set)
    // At least the bit index advances, so this is stateful
    // (though both might happen to return same value for different reasons)
    // The point: the read has side effects (advances bit_index)
    assert_eq!(bus.joypad.snapshot_state().len(), 6,
        "Joypad state exists and is affected by reads");
}

// ═══════════════════════════════════════════════════════════════════
// PURE MEMORY: Where the directed container structure DOES hold
//
// For pure memory (WRAM, ROM, SRAM), reads have no side effects.
// Here the Bus IS a valid directed container, and all five laws hold.
// is_pure_memory() is the predicate that separates the directed
// container from the indexed monad.
// ═══════════════════════════════════════════════════════════════════

#[test]
fn pure_memory_extract_is_idempotent() {
    // For pure memory, consecutive reads at the same address return
    // the same value — extract is idempotent, Law 1 is safe.
    let mut bus = test_bus();
    bus.write(0x7E, 0x0050, 0x42);

    let v1 = bus.read(0x7E, 0x0050);
    let v2 = bus.read(0x7E, 0x0050);
    let v3 = bus.read(0x7E, 0x0050);
    assert_eq!(v1, v2);
    assert_eq!(v2, v3);
    assert_eq!(v1, 0x42,
        "Pure memory: extract is idempotent — directed container laws hold");
}

