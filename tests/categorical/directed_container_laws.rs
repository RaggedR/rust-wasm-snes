//! Directed Container Law Tests for the SNES Bus
//!
//! A directed container (S, P, o, down, +) satisfies five laws.
//! We test whether the SNES memory bus, viewed as a container where
//! shapes = address space configurations and positions = (bank, addr),
//! respects these laws.
//!
//! The key insight: `down` is address decode. Given a position (bank, addr),
//! `down` maps it to a sub-address-space (the target hardware's local view).
//! The `(+)` operation is address composition through the decode chain.
//!
//! Reference: Ahman, Chapman, Uustalu (2014) "When is a container a comonad?"
//!
//! Generated by /snes-category-sweep

use rsnes::bus::Bus;
use rsnes::rom::{Cartridge, MapMode};

fn test_bus() -> Bus {
    let mut rom = vec![0u8; 0x10000]; // 64KB ROM
    // Plant known values for identity checks
    rom[0] = 0x11;         // bank $00, addr $8000 → ROM offset 0
    rom[0x8000] = 0x22;    // bank $01, addr $8000 → ROM offset 0x8000
    rom[0x100] = 0x33;     // bank $00, addr $8100 → ROM offset 0x100
    // Reset vector
    rom[0x7FFC] = 0x00;
    rom[0x7FFD] = 0x80;
    let cart = Cartridge {
        rom,
        sram: vec![0u8; 0x8000], // 32KB SRAM
        title: "DCONT-TEST".to_string(),
        map_mode: MapMode::LoROM,
        rom_size: 0x10000,
        ram_size: 0x8000,
        country: 0,
        version: 0,
        checksum: 0,
        checksum_complement: 0,
    };
    Bus::new(cart)
}

